export function getEmojiByCategory(category: keyof typeof emojiCategories) {
  return emojiCategories[category]?.emojis || [];
}

export function getAllEmojis() {
  return Object.values(emojiCategories).reduce((acc, category) => {
    return acc.concat(category.emojis);
  }, [] as string[]);
}

export function searchEmojis(query: string) {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return frequentlyUsed;

  const allEmojis = getAllEmojis();
  // This is a simple implementation - in a real app you'd want emoji name/keyword data
  return allEmojis.filter((emoji, index) => {
    // Simple search based on position and some common patterns
    const emojiIndex = index.toString();
    return emojiIndex.includes(normalizedQuery) || 
           (normalizedQuery === 'happy' && ['😀', '😊', '😁', '🙂'].includes(emoji)) ||
           (normalizedQuery === 'sad' && ['😢', '😭', '😔', '☹️'].includes(emoji)) ||
           (normalizedQuery === 'love' && ['❤️', '💕', '😍', '🥰'].includes(emoji));
  }).slice(0, 20);
}
